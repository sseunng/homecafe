#!/bin/bash

# 에러 발생 시 스크립트 중단
set -e

# 스크립트 파일이 있는 위치로 이동 (어디서나 실행 가능하도록 설정)
cd "$(dirname "$0")"

echo "============================================="
echo "   [1/3] 기존 도커 컨테이너 중지 (Down)"
echo "============================================="
docker compose down

echo "============================================="
echo "   [2/3] 최신 소스코드 다운로드 (Git Pull)"
echo "============================================="
# 로컬 변경사항과 충돌이 우려될 경우 git pull 전에 stash 하거나 reset할 수 있습니다.
# 여기서는 표준 git pull을 실행합니다.
git pull

echo "============================================="
echo "   [3/3] 도커 빌드 및 백그라운드 실행 (Up & Build)"
echo "============================================="
docker compose up -d --build

echo "============================================="
echo " 🎉 업데이트가 완료되었습니다!"
echo " 포트 3000번에서 서비스가 실행 중입니다."
echo "============================================="
