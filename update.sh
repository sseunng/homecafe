#!/bin/bash

# 에러 발생 시 스크립트 중단
set -e

# 스크립트 파일이 있는 위치로 이동 (어디서나 실행 가능하도록 설정)
cd "$(dirname "$0")"

echo "============================================="
echo "   [1/3] 소스코드 다운로드 (Git Pull)"
echo "============================================="
git pull

# Check if docker and docker compose are available
if command -v docker &> /dev/null && (docker compose version &> /dev/null || command -v docker-compose &> /dev/null); then
  echo "============================================="
  echo "   [2/3] 기존 도커 컨테이너 중지 (Down)"
  echo "============================================="
  docker compose down || true

  echo "============================================="
  echo "   [3/3] 도커 빌드 및 백그라운드 실행 (Up & Build)"
  echo "============================================="
  docker compose up -d --build
  
  echo "============================================="
  echo " 🎉 도커 업데이트가 완료되었습니다!"
  echo "============================================="
else
  echo "============================================="
  echo "   [2/3] 로컬 종속성 설치 (Npm Install)"
  echo "============================================="
  npm install

  echo "============================================="
  echo "   [3/3] 프론트엔드 빌드 (Npm Run Build)"
  echo "============================================="
  npm run build

  echo "============================================="
  echo " 🎉 로컬 호스트 업데이트가 완료되었습니다!"
  echo " 만약 PM2 나 systemd 서비스를 사용 중이시라면 서비스를 재시작해 주세요."
  echo "============================================="
fi
